    public final AccelConstraint fastAccelConstraint =
            new ProfileAccelConstraint(PARAMS.fastMinProfileAccel, PARAMS.fastMaxProfileAccel);

    public final DcMotorEx leftFront, leftBack, rightBack, rightFront;

